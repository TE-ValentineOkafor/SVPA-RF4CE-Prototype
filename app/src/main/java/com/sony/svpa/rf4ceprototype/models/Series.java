package com.sony.svpa.rf4ceprototype.models;

/**
 * Gson class for video series.
 */

public class Series {

  private String id;
  private String name;
  private String attribution;

  public String getId() {
    return id;
  }

  public String getName() {
    return name;
  }

  public String getAttribution() {
    return attribution;
  }
}

package com.sony.svpa.rf4ceprototype.models;

/**
 * Gson class for program seasons.
 */

public class Season {

  private String id;
  private String attribution;
  private int num;
  private int count;

  public String getId() {
    return id;
  }

  public String getAttribution() {
    return attribution;
  }

  public int getNum() {
    return num;
  }

  public int getCount() {
    return count;
  }
}
